
public class Practice {
	
	public static void main(String[] args) {
		int num1 = 7;
		int num2 = 3;
		
		System.out.println("num1 / num2는 : " + (num1 / num2));
		// => 정수형 나눗셈 진행
		System.out.println("num1 / num2는 : " + (7.0 / 3.0));
		// => 실수형 나눗셈 진행
		System.out.println("num1 % num2는 : " + (num1 % num2));
		System.out.println("num1 % num2는 : " + (7.0 % 3.0));
	}

}
